int	ft_atoi(const char *s)
{
	int			z;
	long int	n;
	int			contr;

	z = 1;
	n = 0;
	contr = 0;
	while ((*s == 32) || (*s < 14 && *s > 8))
		s++;
	if ((*s == '+') || (*s == '-'))
		if (*s++ == '-')
			z *= -1;
	while (*s == '0')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		n = n * 10 + (*s++ - '0');
		if (++contr > 19)
		{
			if (z < 1)
				return (0);
			return (-1);
		}
	}
	return ((int)n * z);
}
